from math import ceil,floor
from random import randint
from secrets import randbelow


print(abs(-5.5)) # 절댓갑
print(int(-5.5)) #정수
print(pow(-5.5,2)) #제곱
print(max(-5.5,-0.1)) #더큰거
print(min(1,-5.5)) #더자근거
print(round(-5.5)) #반올림
print(floor(-5.5)) #무조건버림
print(ceil(-5.5)) #무조건올림
print(randint(1,45)) #1과 45 사이 랜덤번호
bunho=2345
print(randbelow(bunho)) # bunho 밑 아무 번호